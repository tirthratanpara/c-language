#include<stdio.h>
void main()
{
	char a;
	printf(" enter a two digit of a day :");
	scanf("%c",&a);
	switch(a)
	{
		case 'm' :
			printf("monday");
			break;
		case 't':
			printf("tuesday");
				break;
		case 'w':
			printf("wednesday");
				break;
		case 'h':
			printf("thrusday");
				break;
		case 'f':
			printf("friday");
				break;
		case 's':
			printf("saturday");
				break;
		case 'u':
			printf("sunday");
				break;
		
			
			
				
	}
}